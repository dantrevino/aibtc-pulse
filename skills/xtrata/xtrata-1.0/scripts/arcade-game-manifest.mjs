import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');
const arcadeRoot = path.join(repoRoot, 'recursive-apps', '21-arcade');
const gamesDir = path.join(arcadeRoot, 'games');
const outputPath = path.join(gamesDir, 'latest-manifest.js');

const GAME_FILE_RE = /^(game\d{2}_[a-z0-9_]+?)(?:-v([a-z0-9][a-z0-9._-]*))?\.js$/i;
const DEFAULT_MAIN_SCRIPT = 'main.js?v=wallet-debug-2026-02-20-12';

function tokenizeVersion(versionRaw){
  if(!versionRaw) return [];
  return String(versionRaw)
    .split(/[._-]+/)
    .filter(Boolean)
    .map((token) => (/^\d+$/.test(token) ? Number(token) : token.toLowerCase()));
}

function compareToken(a, b){
  const isNumA = typeof a === 'number';
  const isNumB = typeof b === 'number';

  if(isNumA && isNumB){
    return a === b ? 0 : (a > b ? 1 : -1);
  }
  if(isNumA !== isNumB){
    return isNumA ? 1 : -1;
  }

  const compare = String(a).localeCompare(String(b));
  if(compare === 0) return 0;
  return compare > 0 ? 1 : -1;
}

function compareVersion(a, b){
  if(!a && !b) return 0;
  if(!a) return -1;
  if(!b) return 1;

  const tokensA = tokenizeVersion(a);
  const tokensB = tokenizeVersion(b);
  const maxLen = Math.max(tokensA.length, tokensB.length);

  for(let i = 0; i < maxLen; i += 1){
    const ta = tokensA[i];
    const tb = tokensB[i];
    if(typeof ta === 'undefined' && typeof tb === 'undefined') return 0;
    if(typeof ta === 'undefined') return -1;
    if(typeof tb === 'undefined') return 1;

    const compare = compareToken(ta, tb);
    if(compare !== 0) return compare;
  }

  return 0;
}

function parseGameFileName(fileName, mtimeMs){
  const match = fileName.match(GAME_FILE_RE);
  if(!match) return null;

  const gameId = match[1];
  const version = match[2] || null;
  const slot = Number(gameId.slice(4, 6));

  return {
    fileName,
    gameId,
    slot,
    version,
    mtimeMs
  };
}

function pickLatest(candidates){
  return [...candidates].sort((a, b) => {
    const byVersion = compareVersion(a.version, b.version);
    if(byVersion !== 0) return -byVersion;

    if(a.mtimeMs !== b.mtimeMs){
      return b.mtimeMs - a.mtimeMs;
    }

    return a.fileName.localeCompare(b.fileName);
  })[0];
}

function buildManifestObject(latestGames){
  const games = latestGames
    .sort((a, b) => a.slot - b.slot)
    .map((entry) => ({
      slot: entry.slot,
      id: entry.gameId,
      version: entry.version,
      file: entry.fileName,
      script: `games/${entry.fileName}`
    }));

  return {
    generatedAt: new Date().toISOString(),
    mainScript: DEFAULT_MAIN_SCRIPT,
    games,
    scripts: games.map((entry) => entry.script)
  };
}

function serializeManifest(manifest){
  const content = JSON.stringify(manifest, null, 2);
  return [
    '/* AUTO-GENERATED by scripts/arcade-game-manifest.mjs. DO NOT EDIT. */',
    'window.ARCADE_GAME_SCRIPT_MANIFEST = Object.freeze(' + content + ');',
    ''
  ].join('\n');
}

export async function generateManifest(){
  const entries = await fs.readdir(gamesDir, { withFileTypes: true });
  const candidatesByGameId = new Map();

  for(const entry of entries){
    if(!entry.isFile()) continue;

    const absolutePath = path.join(gamesDir, entry.name);
    const stat = await fs.stat(absolutePath);
    const parsed = parseGameFileName(entry.name, stat.mtimeMs);
    if(!parsed) continue;

    if(!candidatesByGameId.has(parsed.gameId)){
      candidatesByGameId.set(parsed.gameId, []);
    }
    candidatesByGameId.get(parsed.gameId).push(parsed);
  }

  const latestGames = Array.from(candidatesByGameId.values()).map((group) => pickLatest(group));
  const manifest = buildManifestObject(latestGames);
  const serialized = serializeManifest(manifest);

  await fs.writeFile(outputPath, serialized, 'utf8');
  return manifest;
}

async function runCli(){
  try{
    const manifest = await generateManifest();
    console.log('[arcade-games-manifest] generated', {
      outputPath,
      games: manifest.games.length,
      scripts: manifest.scripts
    });
  }catch(error){
    console.error('[arcade-games-manifest] failed');
    console.error(error);
    process.exitCode = 1;
  }
}

if(process.argv[1] && path.resolve(process.argv[1]) === __filename){
  runCli();
}
