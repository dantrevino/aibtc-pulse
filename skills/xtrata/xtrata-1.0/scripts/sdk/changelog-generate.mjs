#!/usr/bin/env node
import { promises as fs } from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '../..');
const implementationPlanPath = path.join(
  rootDir,
  'docs',
  'sdk',
  'archive',
  'implementation-plan.md'
);
const changelogPath = path.join(rootDir, 'docs', 'sdk', 'changelog.md');
const sdkPackagePath = path.join(rootDir, 'packages', 'xtrata-sdk', 'package.json');
const reconstructionPackagePath = path.join(
  rootDir,
  'packages',
  'xtrata-reconstruction',
  'package.json'
);

const iterationRowPattern =
  /^\|\s*(\d{4}-\d{2}-\d{2}-\d{2})\s*\|\s*([^|]+?)\s*\|\s*(yes|no)\s*\|\s*([^|]+?)\s*\|\s*([^|]+?)\s*\|\s*(pass|fail)\s*\|\s*([^|]+?)\s*\|$/gm;

const trimCell = (value) => value.replace(/\s+/g, ' ').trim();

const readJson = async (filePath) => {
  const raw = await fs.readFile(filePath, 'utf8');
  return JSON.parse(raw);
};

const main = async () => {
  const [planRaw, sdkPackage, reconstructionPackage] = await Promise.all([
    fs.readFile(implementationPlanPath, 'utf8'),
    readJson(sdkPackagePath),
    readJson(reconstructionPackagePath)
  ]);

  const rows = Array.from(planRaw.matchAll(iterationRowPattern)).map((match) => ({
    iteration: trimCell(match[1]),
    scope: trimCell(match[2]),
    apiChange: trimCell(match[3]),
    tests: trimCell(match[4]),
    docs: trimCell(match[5]),
    gate: trimCell(match[6]),
    owner: trimCell(match[7])
  }));

  const sorted = rows.sort((left, right) =>
    left.iteration < right.iteration ? 1 : left.iteration > right.iteration ? -1 : 0
  );

  const now = new Date().toISOString();
  const lines = [
    '# SDK Changelog',
    '',
    `Generated: ${now}`,
    '',
    '## Package versions',
    '',
    `- ${sdkPackage.name}: \`${sdkPackage.version}\``,
    `- ${reconstructionPackage.name}: \`${reconstructionPackage.version}\``,
    '',
    '## Iteration history',
    '',
    '| Iteration | Scope | API change | Gate | Owner |',
    '| --- | --- | --- | --- | --- |',
    ...sorted.map((row) =>
      `| ${row.iteration} | ${row.scope} | ${row.apiChange} | ${row.gate} | ${row.owner} |`
    ),
    '',
    '## Notes',
    '',
    '- Source of truth for historical iteration details remains `docs/sdk/archive/implementation-plan.md`.',
    '- This changelog is generated by `npm run sdk:changelog:generate`.'
  ];

  await fs.writeFile(changelogPath, `${lines.join('\n')}\n`);
  console.log(`[sdk:changelog:generate] PASS (${sorted.length} iterations written)`);
};

main().catch((error) => {
  console.error('[sdk:changelog:generate] FAIL');
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
